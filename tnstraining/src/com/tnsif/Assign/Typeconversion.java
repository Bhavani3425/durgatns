package com.tnsif.Assign;

public class Typeconversion {

	public static void main(String[] args) {
		

			
				//Implicit type conversion
				int a=10;
				double b=a;
				System.out.println(b);
			//Explicit type conversion
			float c=5.82f;
			int s=(int)c;
			System.out.println(s);

		}
	}


